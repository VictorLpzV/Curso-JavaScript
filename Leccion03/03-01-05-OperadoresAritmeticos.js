let a = 3, b=2;

/*Suma*/
let z=a+a;
console.log("Resultado de la suma: " + z);

/*Resta*/
z=a-a;
console.log("Resultado de la suma: " + z);

/*Multiplicacion*/
z=a*a;
console.log("Resultado de la suma: " + z);

/*Divisi√≥n*/
z=a/a;
console.log("Resultado de la suma: " + z);


/*Incremento*/
//pre.incremento (el operador ++antes de la variable)
z= ++a;
console.log(a);
console.log(z);


//post-incremento(el operador ++ despues de la variable)
z= b++;
console.log(b);
console.log(z);


/*Decremento*/
//pre-dencremento (el operador ++antes de la variable)
z= --a;
console.log(a);
console.log(z);


//post-decremento(el operador ++ despues de la variable)
z= b--;
console.log(b);
console.log(z);
